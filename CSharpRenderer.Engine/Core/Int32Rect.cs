namespace CSharpRenderer.Core
{
    public struct Int32Rect
    {
        public int X;

        public int Y;

        public int Width;

        public int Height;

        public Int32Rect(int x, int y, int width, int height)
        {
            X = x;
            Y = y;
            Width = width;
            Height = height;
        }
    }
}
